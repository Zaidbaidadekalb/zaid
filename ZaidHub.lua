			Plus.Position = UDim2.new(0,Mouse.X-button.AbsolutePosition.X,0,Mouse.Y-button.AbsolutePosition.Y)
			until SK == true do
		end
	end)
	
	button.MouseLeave:Connect(function()
		SK = true
